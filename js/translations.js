let currentLang = localStorage.getItem('meal_tracker_lang') || 'en';

export function getLang() {
    return currentLang;
}

export function setLang(lang) {
    if (translations[lang]) {
        currentLang = lang;
        localStorage.setItem('meal_tracker_lang', lang);
        // Dispatch event for instant UI update if needed, though usually we reload
        window.dispatchEvent(new CustomEvent('lang-changed'));
    }
}

export function t(key) {
    const dict = translations[currentLang] || translations['en'];
    return dict[key] || key;
}
